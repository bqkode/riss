// Parse an SVG path-like string of M/L/C/Z commands into segments for canvas drawing
function drawSvgPath(
  ctx: CanvasRenderingContext2D,
  pathData: string,
  offsetX: number,
  offsetY: number,
  scale: number,
): void {
  // Split path into subpaths separated by M commands
  const commands = pathData.match(/[MLCHVSZQTA][^MLCHVSZQTA]*/gi);
  if (!commands) return;

  let curX = 0;
  let curY = 0;

  for (const cmd of commands) {
    const type = cmd[0];
    const args = cmd.slice(1).trim().split(/[\s,]+/).map(Number);

    switch (type) {
      case 'M': {
        // Multiple pairs = moveTo then lineTo
        for (let i = 0; i < args.length; i += 2) {
          curX = args[i];
          curY = args[i + 1];
          if (i === 0) {
            ctx.moveTo(offsetX + curX * scale, offsetY + curY * scale);
          } else {
            ctx.lineTo(offsetX + curX * scale, offsetY + curY * scale);
          }
        }
        break;
      }
      case 'L': {
        for (let i = 0; i < args.length; i += 2) {
          curX = args[i];
          curY = args[i + 1];
          ctx.lineTo(offsetX + curX * scale, offsetY + curY * scale);
        }
        break;
      }
      case 'H': {
        for (let i = 0; i < args.length; i++) {
          curX = args[i];
          ctx.lineTo(offsetX + curX * scale, offsetY + curY * scale);
        }
        break;
      }
      case 'V': {
        for (let i = 0; i < args.length; i++) {
          curY = args[i];
          ctx.lineTo(offsetX + curX * scale, offsetY + curY * scale);
        }
        break;
      }
      case 'C': {
        for (let i = 0; i < args.length; i += 6) {
          ctx.bezierCurveTo(
            offsetX + args[i] * scale,
            offsetY + args[i + 1] * scale,
            offsetX + args[i + 2] * scale,
            offsetY + args[i + 3] * scale,
            offsetX + args[i + 4] * scale,
            offsetY + args[i + 5] * scale,
          );
          curX = args[i + 4];
          curY = args[i + 5];
        }
        break;
      }
      case 'Q': {
        for (let i = 0; i < args.length; i += 4) {
          ctx.quadraticCurveTo(
            offsetX + args[i] * scale,
            offsetY + args[i + 1] * scale,
            offsetX + args[i + 2] * scale,
            offsetY + args[i + 3] * scale,
          );
          curX = args[i + 2];
          curY = args[i + 3];
        }
        break;
      }
      case 'Z':
      case 'z':
        ctx.closePath();
        break;
    }
  }
}

